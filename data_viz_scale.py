def export_episode_for_training(episode_hash, local_dir, export_dir):
    """Export a single episode with EE poses, head pose, and camera frames."""
    output_path = os.path.join(export_dir, f"{episode_hash}.npz")
    if os.path.exists(output_path):
        return output_path  # skip if already exported

    try:
        store = _zarr_export.open_group(str(Path(local_dir) / episode_hash), mode="r")
    except Exception as e:
        print(f"  SKIP {episode_hash}: {e}")
        return None

    try:
        right_ee = np.array(store["right"]["obs_ee_pose"][:])
        head_pose = np.array(store["obs_head_pose"][:])
    except KeyError as e:
        print(f"  SKIP {episode_hash}: missing key {e}")
        return None

    T = min(len(right_ee), len(head_pose))

    # Transform EE to camera frame
    from pyquaternion import Quaternion as Q
    right_ee_cam = np.zeros((T, 3))
    for t in range(T):
        h_pos = head_pose[t, :3]
        h_q = Q(head_pose[t, 3], head_pose[t, 4], head_pose[t, 5], head_pose[t, 6])
        disp = right_ee[t, :3] - h_pos
        right_ee_cam[t] = h_q.inverse.rotate(disp)

    # Try to load camera frames
    images = None
    try:
        if "rgb" in store:
            raw_imgs = store["rgb"][:]
            images = np.zeros((T, 224, 224, 3), dtype=np.uint8)
            for t in range(T):
                if isinstance(raw_imgs[t], bytes):
                    img = simplejpeg.decode_jpeg(raw_imgs[t])
                else:
                    img = np.array(raw_imgs[t])
                if img.shape[:2] != (224, 224):
                    import cv2
                    img = cv2.resize(img, (224, 224))
                images[t] = img
    except Exception as e:
        print(f"  Warning: could not load images for {episode_hash}: {e}")

    save_dict = {
        "right_ee_cam": right_ee_cam,
        "right_ee_world": right_ee[:T],
        "head_poses": head_pose[:T],
        "fps": 30,
        "episode_hash": episode_hash,
    }
    if images is not None:
        save_dict["images"] = images

    np.savez_compressed(output_path, **save_dict)
    return output_path


