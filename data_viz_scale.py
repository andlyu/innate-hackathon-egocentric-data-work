def export_scale_episode(episode_hash, local_dir, output_path=None):
    """Export Scale episode data for combined visualization with MARS arm."""
    if output_path is None:
        output_path = f"{episode_hash}_export.npz"

    store = _zarr_export.open_group(str(Path(local_dir) / episode_hash), mode="r")

    # List all available keys
    print("Available zarr keys:")
    def _list_keys(group, prefix=""):
        for key in group.keys():
            item = group[key]
            if hasattr(item, 'shape'):
                print(f"  {prefix}{key}: shape={item.shape}, dtype={item.dtype}")
            else:
                _list_keys(item, prefix=f"{prefix}{key}/")
    _list_keys(store)

    # Load right hand EE pose and head pose
    right_ee = np.array(store["right"]["obs_ee_pose"][:])
    head_pose = np.array(store["obs_head_pose"][:])

    print(f"\nRight EE pose shape: {right_ee.shape}")
    print(f"Head pose shape: {head_pose.shape}")

    # Transform to camera frame
    from pyquaternion import Quaternion as Q
    T = min(len(right_ee), len(head_pose))
    right_ee_cam = np.zeros((T, 3))

    for t in range(T):
        head_pos = head_pose[t, :3]
        head_q = Q(head_pose[t, 3], head_pose[t, 4], head_pose[t, 5], head_pose[t, 6])
        ee_pos = right_ee[t, :3]
        displacement = ee_pos - head_pos
        right_ee_cam[t] = head_q.inverse.rotate(displacement)

    np.savez(output_path,
             right_ee_cam=right_ee_cam,
             right_ee_world=right_ee[:T],
             head_poses=head_pose[:T],
             fps=30)

    print(f"\nExported {T} frames to {output_path}")
    print("Download this file and run:")
    print(f"  python visualize_combined.py --scale-data {output_path}")

    return output_path

