"""## 8. Visualize

The transform pipeline converts raw zarr poses into a format the renderer understands:

1. **World → camera frame** — EE poses are stored in world frame. `obs_head_pose` (the Aria headset pose) is used to rotate them into camera frame, so they project correctly onto the image.
2. **Interpolate** each arm's action chunk to a fixed 100-step horizon.
3. **Convert** quaternions (`xyzwxyz`) → YPR Euler angles.
4. **Concatenate** left + right into `actions_cartesian`.
"""

from egomimic.rldb.zarr.action_chunk_transforms import (
    ActionChunkCoordinateFrameTransform,
    DeleteKeys,
    PoseCoordinateFrameTransform,
)


def _scale_cartesian_keymap():
    """Cartesian keymap extended with obs_head_pose for the world→camera transform."""
    key_map = Scale.get_keymap(keymap_mode="cartesian")
    key_map["obs_head_pose"] = {"key_type": "proprio_keys", "zarr_key": "obs_head_pose"}
    return key_map


def _build_scale_viz_transform_list(chunk_length: int = 100) -> list:
    head       = "obs_head_pose"
    la, ra     = "left.action_ee_pose",      "right.action_ee_pose"
    lo, ro     = "left.obs_ee_pose",         "right.obs_ee_pose"
    la_c, ra_c = "left.action_ee_pose_cam",  "right.action_ee_pose_cam"
    lo_c, ro_c = "left.obs_ee_pose_cam",     "right.obs_ee_pose_cam"
    return [
        # 1. Rotate from world frame into camera frame using the headset pose.
        ActionChunkCoordinateFrameTransform(
            target_world=head, chunk_world=la, transformed_key_name=la_c, mode="xyzwxyz"),
        ActionChunkCoordinateFrameTransform(
            target_world=head, chunk_world=ra, transformed_key_name=ra_c, mode="xyzwxyz"),
        PoseCoordinateFrameTransform(
            target_world=head, pose_world=lo, transformed_key_name=lo_c, mode="xyzwxyz"),
        PoseCoordinateFrameTransform(
            target_world=head, pose_world=ro, transformed_key_name=ro_c, mode="xyzwxyz"),
        # 2. Interpolate action chunk to fixed horizon.
        InterpolatePose(new_chunk_length=chunk_length, action_key=la_c,
                        output_action_key=la_c, stride=Scale.ACTION_STRIDE, mode="xyzwxyz"),
        InterpolatePose(new_chunk_length=chunk_length, action_key=ra_c,
                        output_action_key=ra_c, stride=Scale.ACTION_STRIDE, mode="xyzwxyz"),
        # 3. Quaternion → YPR.
        XYZWXYZ_to_XYZYPR(keys=[la_c, ra_c, lo_c, ro_c]),
        # 4. Concatenate both arms.
        ConcatKeys(key_list=[la_c, ra_c], new_key_name="actions_cartesian", delete_old_keys=True),
        ConcatKeys(key_list=[lo_c, ro_c], new_key_name="observations.state.ee_pose", delete_old_keys=True),
        DeleteKeys(keys_to_delete=[la, ra, lo, ro, head]),
    ]


def visualize_scale_data(
    episode_hash: str,
    temp_dir: str,
    output_path: str | None = None,
    mode: Literal["traj", "axes", "annotations"] = "traj",
    max_frames: int = 10_000,
    fps: int = 30,
    stride: int = 1,
    sync_from_s3: bool = False,
):
    """Render a Scale episode to an mp4 and return the frames as a numpy array.

    stride: keep every Nth source frame. Output fps is adjusted to fps/stride so
            real-time playback speed is unchanged (e.g. stride=3 renders 3× faster
            and writes at 10 fps instead of 30 fps).
    """
    load_env()

    if output_path is None:
        output_path = str(Path(temp_dir) / f"{episode_hash}_{mode}.mp4")

    if mode == "annotations":
        key_map = Scale.get_keymap(keymap_mode="cartesian", annotation_key="annotations")
        transform_list, viz_batch_key = None, "annotations"
    else:
        key_map = _scale_cartesian_keymap()
        transform_list, viz_batch_key = _build_scale_viz_transform_list(), "actions_cartesian"

    filters = DatasetFilter(
        filter_lambdas=[f"lambda row: row['episode_hash'] in {set([episode_hash])!r}"]
    )
    ResolverCls = S3EpisodeResolver if sync_from_s3 else LocalEpisodeResolver
    resolver = ResolverCls(temp_dir, key_map=key_map, transform_list=transform_list)
    multi_ds = MultiDataset._from_resolver(
        resolver, filters=filters, sync_from_s3=sync_from_s3, mode="total"
    )

    # Pre-load annotation spans from zarr so we can number them (0., 1., …)
    import zarr as _zarr, json as _json
    _ann_spans: list[dict] = []
    try:
        _store = _zarr.open_group(str(Path(temp_dir) / episode_hash), mode="r")
        for _x in _store["annotations"][:]:
            if isinstance(_x, bytes):
                _x = _x.decode()
            try:
                _ann_spans.append(_json.loads(_x))
            except Exception:
                pass
    except Exception:
        pass

    def _ann_texts(frame_idx: int) -> list[str]:
        return [
            f"{idx}. {span.get('text', '')}"
            for idx, span in enumerate(_ann_spans)
            if int(span.get("start_idx", -1)) <= frame_idx < int(span.get("end_idx", -1))
        ]

    loader = torch.utils.data.DataLoader(multi_ds, batch_size=1, shuffle=False)
    total_source = min(len(multi_ds), max_frames)
    n_rendered = (total_source + stride - 1) // stride
    images = []

    try:
        from tqdm.auto import tqdm as _tqdm
        _iter = _tqdm(loader, total=total_source, desc=f"Rendering {episode_hash[:16]}…", unit="frame")
    except ImportError:
        _iter = loader

    for i, batch in enumerate(_iter):
        if i >= max_frames:
            break
        if i % stride != 0:
            continue
        vis = Scale.viz_transformed_batch(batch, mode=mode, viz_batch_key=viz_batch_key)
        texts = _ann_texts(i)
        if texts:
            vis = _viz_annotations(vis, texts)
        images.append(vis)
        n_kept = len(images)
        if n_kept % max(1, n_rendered // 20) == 0 or n_kept == n_rendered:
            print(f"\rFrame {n_kept}/{n_rendered}", end="", flush=True)
    print()

    if not images:
        raise RuntimeError(f"No frames rendered for episode '{episode_hash}'.")

    images = np.stack(images, axis=0)
    out_fps = fps / stride  # preserve real-time playback speed
    writer = imageio.get_writer(output_path, fps=out_fps)
    for frame in images:
        writer.append_data(frame)
    writer.close()

    print(f"Saved {len(images)} frames at {out_fps:.1f} fps → {output_path}")
    return images

